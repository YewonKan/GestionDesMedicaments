    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        fm = new FonctionsMetier();
        ConnexionBdd cnx = new ConnexionBdd();

        for (Medicament md : fm.getAllMedicament()) {
            cmbMedicament.addItem(md.getNomMedicament());
        }

        for (TypeIndividu ty : fm.getAllIndividu()) {
            cmbTypePerso.addItem(ty.getTIlibelle());
        }

        txtdosage.setText("0");
    }//GEN-LAST:event_formWindowOpened

    private void btnPlusMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnPlusMouseClicked
        int a = Integer.parseInt(txtdosage.getText());
        a = a + 1;
        txtdosage.setText(String.valueOf(a));
    }//GEN-LAST:event_btnPlusMouseClicked

    private void btnmoinsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnmoinsMouseClicked
        int a = Integer.parseInt(txtdosage.getText());
        if (a == 0) {
            JOptionPane.showMessageDialog(this, "Le dosage est déjà 0, nous ne pouvons pas soustraire", "Erreur de saisie", JOptionPane.ERROR_MESSAGE);
        }
        else {
            a = a - 1;
            txtdosage.setText(String.valueOf(a));
        }

    }//GEN-LAST:event_btnmoinsMouseClicked

    private void btnInsertMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnInsertMouseClicked

        if (txtdosage.getText().compareTo("0") == 0) {
            JOptionPane.showMessageDialog(this, "Inserer un dosage", "Erreur de saisie", JOptionPane.ERROR_MESSAGE);
        }
        else if (txtPosologie.getText().compareTo("") == 0) {
            JOptionPane.showMessageDialog(this, "Ecrire la fréquence de la prise du medicament", "Erreur de saisie", JOptionPane.ERROR_MESSAGE);
        }
        else {
            int MedicamentIndex = cmbMedicament.getSelectedIndex() + 1;
            int TypeIndex = cmbTypePerso.getSelectedIndex() + 1;
            int userDosage = Integer.parseInt(txtdosage.getText());
            String userPosologie = txtPosologie.getText();
            Prescrire userPrescription = new Prescrire(MedicamentIndex, TypeIndex, userDosage, userPosologie);
            fm.setPrescrire(userPrescription);
            JOptionPane.showMessageDialog(this, "Succeed", "Succeed", JOptionPane.INFORMATION_MESSAGE);

            frmPrescription frm = new frmPrescription();
            frm.setVisible(true);
            dispose();
        }


    }//GEN-LAST:event_btnInsertMouseClicked

